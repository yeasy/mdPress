// TestGeneratePreviewHTML_ValidHTML tests that generated HTML is well-formed
func TestGeneratePreviewHTML_ValidHTML(t *testing.T) {
	themes := []Theme{
		{
			Name:        "test",
			DisplayName: "Test",
			Description: "Desc",
			Author:      "Auth",
			Version:     "1.0",
			Colors: ThemeColors{
				Primary:    "#111111",
				Secondary:  "#222222",
				Accent:     "#333333",
				Text:       "#000000",
				Background: "#FFFFFF",
				CodeBg:     "#F0F0F0",
			},
		},
	}

	result := generatePreviewHTML(themes)

	// Check opening and closing tags match
	htmlOpen := strings.Count(result, "<html")
	htmlClose := strings.Count(result, "</html>")
	if htmlOpen != htmlClose {
		t.Error("generatePreviewHTML() html tags should match")
	}

	bodyOpen := strings.Count(result, "<body")
	bodyClose := strings.Count(result, "</body>")
	if bodyOpen != bodyClose {
		t.Error("generatePreviewHTML() body tags should match")
	}

	headOpen := strings.Count(result, "<head")
	headClose := strings.Count(result, "</head>")
	if headOpen != headClose {
		t.Error("generatePreviewHTML() head tags should match")
	}
}

