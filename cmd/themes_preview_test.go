// TestGeneratePreviewHTML_Basic tests basic HTML generation for themes preview
func TestGeneratePreviewHTML_Basic(t *testing.T) {
	themes := []Theme{
		{
			Name:        "test",
			DisplayName: "Test Theme",
			Description: "A test theme",
			Author:      "Test Author",
			Version:     "1.0.0",
			Colors: ThemeColors{
				Primary:    "#FF0000",
				Secondary:  "#00FF00",
				Accent:     "#0000FF",
				Text:       "#000000",
				Background: "#FFFFFF",
				CodeBg:     "#F0F0F0",
			},
		},
	}

	result := generatePreviewHTML(themes)

	// Verify HTML structure
	checks := []struct {
		name    string
		content string
	}{
		{"doctype", "<!DOCTYPE html>"},
		{"html lang", `<html lang="en">`},
		{"charset", `<meta charset="UTF-8">`},
		{"title", "<title>mdpress Themes Preview</title>"},
		{"header", "mdpress Theme Showcase"},
		{"container div", `<div class="container">`},
		{"themes grid", `<div class="themes-grid">`},
		{"footer", "Generated by mdpress"},
		{"closing html", "</html>"},
	}

	for _, check := range checks {
		if !strings.Contains(result, check.content) {
			t.Errorf("generatePreviewHTML() should contain %q, got output: %s", check.name, result[:200])
		}
	}
}

